        }
        $widget_elements = get_option('codexse_widgets'); // Make sure this is an array or handle as needed
        return isset($widget_elements[$key]) ? $widget_elements[$key] : null;
    }

    public static function get_features_status($key) {
        if (empty($key)) {
            return null;
        }
        $feature_elements = get_option('codexse_features'); // Make sure this is an array or handle as needed
        return isset($feature_elements[$key]) ? $feature_elements[$key] : null;
    }

    public static function elementor_version_check( $operator = '<', $version = '2.6.0' ) {
        return defined( 'ELEMENTOR_VERSION' ) && version_compare( ELEMENTOR_VERSION, $version, $operator );
